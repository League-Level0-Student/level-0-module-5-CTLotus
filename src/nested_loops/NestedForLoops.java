package nested_loops;

public class NestedForLoops {
	public static void main(String[] args) {
		for (int i = 0; i < 3; i++) {
			for (int j = 0; j < 3; j++) {
				System.out.println(i + ""+ j);
			}
		}
		for (int o = 0; o < 4; o++) {
			for (int i = 4; i < 7; i++) {
				System.out.println();
			}
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
